def load_data(train_dir, test_dir, batch_size):
    AUTOTUNE = tf.data.AUTOTUNE
    SEED = 42

    raw_train_ds = tf.keras.utils.text_dataset_from_directory(
        train_dir,
        batch_size=batch_size,
        validation_split=0.2,
        subset='training',
        seed=SEED)

    class_names = raw_train_ds.class_names
    train_ds = raw_train_ds.cache().prefetch(buffer_size=AUTOTUNE)

    val_ds = tf.keras.utils.text_dataset_from_directory(
        train_dir,
        batch_size=batch_size,
        validation_split=0.2,
        subset='validation',
        seed=SEED)
    val_ds = val_ds.cache().prefetch(buffer_size=AUTOTUNE)

    test_ds = tf.keras.utils.text_dataset_from_directory(
        test_dir,
        batch_size=batch_size)
    test_ds = test_ds.cache().prefetch(buffer_size=AUTOTUNE)

    return train_ds, val_ds, test_ds, class_names

def print_example_sentences(train_ds, class_names):
    print("EXAMPLE training/test sentences...")
    for text_batch, label_batch in train_ds.take(1):
        for i in range(3):
            print(f'Review: {text_batch.numpy()[i]}')
            label = label_batch.numpy()[i]
            print(f'Label : {label} ({class_names[label]})')

def create_classifier_model(bert_model_name):
    # specify the BERT model to use from the available ones
    tfhub_handle_encoder = map_name_to_handle[bert_model_name]
    tfhub_handle_preprocess = map_model_to_preprocess[bert_model_name]

    print("CREATING classifier model...")
    text_input = tf.keras.layers.Input(shape=(), dtype=tf.string, name='text')
    preprocessing_layer = hub.KerasLayer(tfhub_handle_preprocess, name='preprocessing')
    encoder_inputs = preprocessing_layer(text_input)
    encoder = hub.KerasLayer(tfhub_handle_encoder, trainable=True, name='BERT_encoder')
    outputs = encoder(encoder_inputs)
    net = outputs['pooled_output']
    net = tf.keras.layers.Dropout(0.1)(net)
    net = tf.keras.layers.Dense(1, activation=None, name='classifier')(net)
    model = tf.keras.Model(inputs=text_input, outputs=net)
    model.summary()
    return model

def run_experiment(train_dir, test_dir, batch_size, epochs, bert_model_name):
    train_ds, val_ds, test_ds, class_names = load_data(train_dir, test_dir, batch_size)
    print_example_sentences(train_ds, class_names)
	
    loss = tf.keras.losses.BinaryCrossentropy(from_logits=True)
    metrics = tf.metrics.BinaryAccuracy()
    model = create_classifier_model(bert_model_name)

    steps_per_epoch = tf.data.experimental.cardinality(train_ds).numpy()
    num_train_steps = steps_per_epoch * epochs
    num_warmup_steps = int(0.1*num_train_steps)
    optimizer = optimization.create_optimizer(init_lr=3e-5,
                                          num_train_steps=num_train_steps,
                                          num_warmup_steps=num_warmup_steps,
                                          optimizer_type='adamw')

    print("TRAINING & EVALUATING classifier model...")
    model.compile(optimizer=optimizer, loss=loss, metrics=metrics)
    model.fit(x=train_ds, validation_data=val_ds, epochs=epochs)
    _, accuracy = model.evaluate(test_ds)
    print("Test Accuracy: %s" (accuracy))


